package com.programmingfree.model;

public class Mesa {

	private int IdMesa;
	private int NumMaxComensaja;
	private String Ubicacion;
	
	
	public int getIdMesa() {
		return IdMesa;
	}
	public void setIdMesa(int idMesa) {
		IdMesa = idMesa;
	}
	public int getNumMaxComensaja() {
		return NumMaxComensaja;
	}
	public void setNumMaxComensaja(int numMaxComensaja) {
		NumMaxComensaja = numMaxComensaja;
	}
	public String getUbicacion() {
		return Ubicacion;
	}
	public void setUbicacion(String ubicacion) {
		Ubicacion = ubicacion;
	}
	
	
	
	
}
